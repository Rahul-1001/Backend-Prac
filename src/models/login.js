const mongoose=require("mongoose")
const loginSchema=new mongoose.Schema({
    firstName:{
        type:String,
        required:true,
        trim:true
    },
    lastName:{
        type:String,
        required:true,
        trim:true
    },
    email:{
        type:String,
        required:true,
        trim:true
    },
    password:{
        type:String,
        required:true
    }
}) 

const Login=mongoose.model("Login",loginSchema);
module.exports=Login;